function Get-AoC2025Day4Answer {
    [CmdletBinding()]
    param (
        [Parameter(Mandatory = $true, Position = 0)]
        [ValidateNotNullOrEmpty()]
        [String]$InputObject
    )

    [String[]]$rows = $InputObject -split ([System.Environment]::NewLine)
    [Char[][]]$grid = [Char[][]]::new($rows.Count, $rows[0].Length)

    for ($row = 0; $row -lt $rows.Count; $row ++) {
        $grid[$row] = $rows[$row].ToCharArray()
    }

    [Int32]$firstRun = 0
    [Int32]$removedRolls = 0

    $ret = Get-AccessibleRolls -Grid $grid

    $firstRun = $ret.AccessibleRolls
    $removedRolls = $firstRun

    while ($ret.AccessibleRolls -gt 0) {
        $ret = Get-AccessibleRolls -Grid $ret.Grid
        $removedRolls += $ret.AccessibleRolls
    }

    return [PSCustomObject]@{
        'Part 1' = $firstRun
        'Part 2' = $removedRolls
    }
}

