<#
Top most: index the length of the first row and anything less than that length is on the top row
Bottom most: Using the index for the top row, subtract it from the .Count of the array. Anything greater than that value is on the bottom row
Left: Anything divisble by the length of the array on the left
Right:
#>

function Get-AccessibleRolls {
    [CmdletBinding()]
    param (
        [Parameter(Mandatory = $true, Position = 0)]
        [ValidateNotNullOrEmpty()]
        [Char[][]]$Grid
    )

    [Char[][]]$tmpGrid = [Char[][]]::new($Grid.Count, $Grid[0].Count)
    [Int32]$accessibleRolls = 0

    for ($row = 0; $row -lt $Grid.Count; $row ++) {
        for ($column = 0; $column -lt $Grid[$row].Count; $column ++) {
            if ($Grid[$row][$column] -eq '@') {
                [Int32]$adjacentRolls = 0

                # Skip if right-most
                if ($column -lt ($Grid[$row].Count -1)) {
                    # Right
                    if ($Grid[$row][$column + 1] -eq '@') {
                        $adjacentRolls ++
                    }

                    # Top right
                    if (($row -ne 0) -and ($Grid[$row - 1][$column + 1] -eq '@')) {
                        $adjacentRolls ++
                    }

                    # Bottom right
                    if (($row -lt ($Grid.Count -1)) -and ($Grid[$row + 1][$column + 1] -eq '@')) {
                        $adjacentRolls ++
                    }
                }

                # Top
                if (($row -ne 0) -and ($Grid[$row - 1][$column] -eq '@')) {
                    $adjacentRolls ++
                }

                # Bottom
                if (($row -lt ($Grid.Count -1)) -and ($Grid[$row + 1][$column] -eq '@')) {
                    $adjacentRolls ++
                }

                # Skip if current character is left-most
                if ($column -ne 0) {
                    # Left
                    if ($Grid[$row][$column - 1] -eq '@') {
                        $adjacentRolls ++
                    }

                    # Bottom left
                    if (($row -lt ($Grid.Count -1)) -and ($Grid[$row + 1][$column -1] -eq '@')) {
                        $adjacentRolls ++
                    }

                    # Top left
                    if (($row -ne 0) -and ($Grid[$row - 1][$column - 1] -eq '@')) {
                        $adjacentRolls ++
                    }
                }

                if ($adjacentRolls -lt 4) {
                    $accessibleRolls ++
                    $tmpGrid[$row][$column] = 'X'
                } else {
                    $tmpGrid[$row][$column] = '@'
                }
            } else {
                $tmpGrid[$row][$column] = '.'
            }
        }
    }

    return [PSCustomObject]@{
        AccessibleRolls = $accessibleRolls
        Grid = $tmpGrid
    }
}

